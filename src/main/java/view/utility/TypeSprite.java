package view.utility;

/**
 * Types of Sprite that can be drawn.
 *
 */
public enum TypeSprite {
    /**
     *  Low range type.
     */
    SOURCELOW, 
    /**
     *  Medium range type.
     */
    SOURCEMID, 
    /**
     *  High range type.
     */
    SOURCEHIGH,
    /**
     *  Full range type.
     */
    SOURCEFULL,
    /**
     *  listener.
     */
    LISTENER,
    /**
     *  listener for cinema env.
     */
    LISTENERCINEMA;
}
